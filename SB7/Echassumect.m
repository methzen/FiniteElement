function [E13,E23]=Echassumect(DSP,XYZ,XI)
%
%%
XIA1=[0.5 0 -1];
XIA2=[0.5 0 +1];
XIB1=[0 0.5 -1];
XIB2=[0 0.5 +1];
XIC1=[0.5 0.5 -1];
XIC2=[0.5 0.5 +1];
%
[E13A1,E23A1]=Ect(DSP,XYZ,XIA1);
[E13B1,E23B1]=Ect(DSP,XYZ,XIB1);
[E13C1,E23C1]=Ect(DSP,XYZ,XIC1);
%
[E13A2,E23A2]=Ect(DSP,XYZ,XIA2);
[E13B2,E23B2]=Ect(DSP,XYZ,XIB2);
[E13C2,E23C2]=Ect(DSP,XYZ,XIC2);
%
% FACE INF
E23_01=E23B1;
E23_xi1=-(E23B1-E13A1-E23C1+E13C1);
E23_1=E23_01+E23_xi1*XI(1);
%
E13_01=E13A1;
E13_eta1=(E23B1-E13A1-E23C1+E13C1);
E13_1=E13_01+E13_eta1*XI(2);
% FACE SUP
E23_02=E23B2;
E23_xi2=-(E23B2-E13A2-E23C2+E13C2);
E23_2=E23_02+E23_xi2*XI(1);
%
E13_02=E13A2;
E13_eta2=(E23B2-E13A2-E23C2+E13C2);
E13_2=E13_02+E13_eta2*XI(2);
%
E13=0.5*(1-XI(3))*E13_1+0.5*(1+XI(3))*E13_2;
E23=0.5*(1-XI(3))*E23_1+0.5*(1+XI(3))*E23_2;

end